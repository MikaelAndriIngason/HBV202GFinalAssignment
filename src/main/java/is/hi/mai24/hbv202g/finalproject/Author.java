package is.hi.mai24.hbv202g.finalproject;

public class Author {
    // Variables
    private String name;

    /**
     * Author constructor (creates a new author).
     * @param name author's name
     */
    public Author(String name) {
        this.name = name;
    }

    // Getters and setters

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
